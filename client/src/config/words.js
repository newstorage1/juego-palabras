// Función para obtener palabras por tema
export const getWordsByTheme = (language, theme) => {
  return wordThemes[language]?.[theme] || wordThemes.en.technology;
};

// Función para obtener todos los temas disponibles
export const getAvailableThemes = (language) => {
  return Object.keys(wordThemes[language] || wordThemes.en);
};

// Función para mezclar palabras al azar
export const shuffleWords = (words) => {
  return [...words].sort(() => 0.5 - Math.random());
};

// Función para seleccionar palabras al azar
export const getRandomWords = (language, theme, count = 20) => {
  const words = getWordsByTheme(language, theme);
  const shuffled = shuffleWords(words);
  return shuffled.slice(0, count);
};
